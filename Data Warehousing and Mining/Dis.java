class FunDis
{
	void sat()
	{
		double a[][]={{0.4,0.53},{0.22,0.38},{0.35,0.32},{0.26,0.19},{0.08,0.41},{0.45,0.30}};
		int n=6;
		double a1[][]=new double[n][n];
		for(int i=0;i<n-1;i++)
		{
			for(int k=i+1;k<n;k++)
			{
				int j=0;
					double r=((a[i][j]-a[k][j])*(a[i][j]-a[k][j]));
					double r1=((a[i][j+1]-a[k][j+1])*(a[i][j+1]-a[k][j+1]));
					double ans=Math.sqrt(r+r1);
					a1[k][i]=ans;
					a1[i][k]=ans;
			}
		}
		for(int i=0;i<n;i++)
		{
			for(int k=0;k<n;k++)
			{
				System.out.print("  "+a1[i][k]);
			}
			System.out.println("\n ");
		}
	}
	
}

